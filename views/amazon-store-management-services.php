<script>
  function getPostsPerPage() {
    return window.innerWidth <= 767 ? 3 : (window.innerWidth < 1601 ? 3 : 3);
  }

  const apiURL = `https://yourbrand.com/blog/wp-json/wp/v2/posts?per_page=${getPostsPerPage()}&_embed`;

  axios.get(apiURL)
    .then(response => {
      const posts = response.data;
      const desktopContainer = document.getElementById('blog-posts');
      const mobileTrack = document.querySelector('.slider-track');
      const loader = document.getElementById('loader');
      loader.style.display = 'none';

      posts.forEach(post => {
        const title = post.title.rendered;
        const excerpt = post.excerpt.rendered;
        const link = post.link;
        let imageURL = '';

        if (post._embedded?.['wp:featuredmedia']?.[0]?.source_url) {
          imageURL = post._embedded['wp:featuredmedia'][0].source_url;
        }

        // Desktop version
        desktopContainer.innerHTML += `
          <div class="post">
            ${imageURL ? `<img src="${imageURL}" alt="Post image">` : ''}
            <div class="post-content">
              <h2>${title}</h2>
              <div>${excerpt}</div>
              <a href="${link}" target="_blank" class="frame-9">Read More</a>
            </div>
          </div>
        `;

        // Mobile version
        mobileTrack.innerHTML += `
          <div class="slide">
            ${imageURL ? `<img src="${imageURL}" alt="Post image">` : ''}
            <div class="post-content">
              <h2>${title}</h2>
              <div>${excerpt}</div>
              <a href="${link}" target="_blank" class="frame-9">Read More</a>
            </div>
          </div>
        `;
      });

      // Initialize slider
      let currentSlide = 0;
      const track = document.querySelector('.slider-track');
      const slides = document.querySelectorAll('.slide');
      const totalSlides = slides.length;

      document.querySelector('.slider-next').addEventListener('click', () => {
        if (currentSlide < totalSlides - 1) {
          currentSlide++;
          track.style.transform = `translateX(-${currentSlide * 100}%)`;
        }
      });

      document.querySelector('.slider-prev').addEventListener('click', () => {
        if (currentSlide > 0) {
          currentSlide--;
          track.style.transform = `translateX(-${currentSlide * 100}%)`;
        }
      });
    })
    .catch(error => {
      document.getElementById('loader').textContent = 'Failed to load posts.';
      console.error('Error:', error);
    });

  // Handle window resize
  window.addEventListener('resize', () => {
    if (window.innerWidth <= 767) {
      document.querySelector('.slider-track').style.transform = 'translateX(0)';
    }
  });

</script>


